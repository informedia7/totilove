-- Table to track which receivers should see which deleted users in their conversation list
-- This allows us to hard delete all messages but still show "Account Deactivated" for receivers
-- who had conversations with the deleted user

CREATE TABLE IF NOT EXISTS users_deleted_receivers (
    id SERIAL PRIMARY KEY,
    deleted_user_id INTEGER NOT NULL,
    receiver_id INTEGER NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UNIQUE(deleted_user_id, receiver_id)
);

-- Create indexes for faster lookups
CREATE INDEX IF NOT EXISTS idx_users_deleted_receivers_deleted_user ON users_deleted_receivers(deleted_user_id);
CREATE INDEX IF NOT EXISTS idx_users_deleted_receivers_receiver ON users_deleted_receivers(receiver_id);

-- Add table comment
COMMENT ON TABLE users_deleted_receivers IS 'Tracks which receivers should see which deleted users in their conversation list. Populated BEFORE hard deleting messages, so we know who had conversations even after messages are deleted.';































































































































































































