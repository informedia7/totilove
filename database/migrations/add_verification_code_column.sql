-- Add verification_code column to email_verification_tokens table
-- This allows users to verify their email using either a link or a 6-digit code

ALTER TABLE email_verification_tokens 
ADD COLUMN IF NOT EXISTS verification_code VARCHAR(6);

-- Create index on verification_code for fast lookups
CREATE INDEX IF NOT EXISTS idx_email_verification_tokens_code ON email_verification_tokens(verification_code);

-- Add comment
COMMENT ON COLUMN email_verification_tokens.verification_code IS '6-digit verification code for manual entry';


























































































































